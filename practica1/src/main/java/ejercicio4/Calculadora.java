package ejercicio4;

public class Calculadora {

    public int sumar(int i, int y) {
        return i + y;
    }

    public int sumar(int i, int y, int z) {
        return i + y + z;
    }

    public double sumar(double v, double w) {
        return (v + w);
    }

    public int multiplicar(int i, int y) {
        return i * y;
    }

    public double multiplicar(int i, double y) {
        return (i * y);
    }
}
